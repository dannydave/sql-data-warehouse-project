/*
===============================================================================
Stored Procedure: bronze.load_bronze
===============================================================================
Purpose:
    Loads data into the Bronze Layer from external CSV files using BULK INSERT.
    This is the initial stage of the data pipeline (Source â†’ Bronze).

Functionality:
    - Truncates existing data from bronze tables to ensure a clean load.
    - Loads data from specified source CSV files into corresponding bronze tables.
